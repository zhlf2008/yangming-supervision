CREATE OR REPLACE FUNCTION app_private.can_access_reading_media_object(
  object_name TEXT,
  target_module_key TEXT
)
RETURNS BOOLEAN
LANGUAGE sql
STABLE
SECURITY DEFINER
SET search_path = public, storage
AS $$
  WITH path_parts AS (
    SELECT storage.foldername(object_name) AS parts
  )
  SELECT
    auth.uid() IS NOT NULL
    AND COALESCE(array_length(parts, 1), 0) >= 2
    AND parts[1] ~ '^[0-9]+$'
    AND parts[2] ~ '^[0-9]+$'
    AND app_private.has_module_org_scope(
      parts[1]::BIGINT,
      target_module_key,
      parts[2]::BIGINT,
      NULL
    )
  FROM path_parts;
$$;

REVOKE ALL ON FUNCTION app_private.can_access_reading_media_object(TEXT, TEXT)
FROM PUBLIC;
GRANT EXECUTE ON FUNCTION app_private.can_access_reading_media_object(TEXT, TEXT)
TO authenticated;

