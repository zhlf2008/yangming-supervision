CREATE TABLE IF NOT EXISTS study_reading_media_assets (
  id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
  semester_id BIGINT NOT NULL REFERENCES semesters(id) ON DELETE CASCADE,
  schedule_instance_id BIGINT NOT NULL REFERENCES study_schedule_instances(id) ON DELETE CASCADE,
  org_id BIGINT NOT NULL REFERENCES organizations(id) ON DELETE CASCADE,
  asset_kind TEXT NOT NULL DEFAULT 'other'
    CHECK (asset_kind IN ('role', 'overview', 'moment', 'other')),
  demand_id BIGINT REFERENCES study_assignment_demands(id) ON DELETE SET NULL,
  assignment_person_id BIGINT REFERENCES study_assignment_people(id) ON DELETE SET NULL,
  role_name_snapshot TEXT NOT NULL DEFAULT '',
  person_name_snapshot TEXT NOT NULL DEFAULT '',
  caption TEXT NOT NULL DEFAULT '',
  storage_path TEXT NOT NULL UNIQUE,
  file_name TEXT NOT NULL DEFAULT '',
  mime_type TEXT NOT NULL DEFAULT 'image/jpeg',
  file_size BIGINT NOT NULL DEFAULT 0 CHECK (file_size >= 0),
  image_width INTEGER CHECK (image_width IS NULL OR image_width > 0),
  image_height INTEGER CHECK (image_height IS NULL OR image_height > 0),
  sort_order INTEGER NOT NULL DEFAULT 0,
  submitted_by UUID NOT NULL REFERENCES profiles(id) ON DELETE RESTRICT,
  created_at TIMESTAMPTZ NOT NULL DEFAULT NOW(),
  updated_at TIMESTAMPTZ NOT NULL DEFAULT NOW()
);

CREATE INDEX IF NOT EXISTS idx_reading_media_assets_schedule
ON study_reading_media_assets(semester_id, schedule_instance_id, org_id, sort_order, id);

CREATE INDEX IF NOT EXISTS idx_reading_media_assets_demand
ON study_reading_media_assets(demand_id, assignment_person_id);

CREATE INDEX IF NOT EXISTS idx_reading_media_assets_submitted_by
ON study_reading_media_assets(submitted_by);

CREATE TABLE IF NOT EXISTS publicity_reading_posters (
  id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
  semester_id BIGINT NOT NULL REFERENCES semesters(id) ON DELETE CASCADE,
  schedule_instance_id BIGINT NOT NULL REFERENCES study_schedule_instances(id) ON DELETE CASCADE,
  org_id BIGINT NOT NULL REFERENCES organizations(id) ON DELETE CASCADE,
  title TEXT NOT NULL DEFAULT '',
  subtitle TEXT NOT NULL DEFAULT '',
  closing_text TEXT NOT NULL DEFAULT '在共读中照见彼此，在践行中抵达良知。',
  participant_count INTEGER CHECK (participant_count IS NULL OR participant_count >= 0),
  layout_style TEXT NOT NULL DEFAULT 'auto'
    CHECK (layout_style IN ('auto', 'compact', 'editorial', 'panorama')),
  status TEXT NOT NULL DEFAULT 'draft'
    CHECK (status IN ('draft', 'ready', 'published')),
  created_by UUID NOT NULL REFERENCES profiles(id) ON DELETE RESTRICT,
  updated_by UUID NOT NULL REFERENCES profiles(id) ON DELETE RESTRICT,
  created_at TIMESTAMPTZ NOT NULL DEFAULT NOW(),
  updated_at TIMESTAMPTZ NOT NULL DEFAULT NOW(),
  UNIQUE (schedule_instance_id, org_id)
);

CREATE INDEX IF NOT EXISTS idx_publicity_reading_posters_scope
ON publicity_reading_posters(semester_id, org_id, schedule_instance_id, status);

CREATE TABLE IF NOT EXISTS publicity_reading_poster_items (
  id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
  poster_id BIGINT NOT NULL REFERENCES publicity_reading_posters(id) ON DELETE CASCADE,
  asset_id BIGINT NOT NULL REFERENCES study_reading_media_assets(id) ON DELETE CASCADE,
  slot_kind TEXT NOT NULL DEFAULT 'auto'
    CHECK (slot_kind IN ('auto', 'feature', 'role', 'overview', 'moment')),
  sort_order INTEGER NOT NULL DEFAULT 0,
  crop_x NUMERIC(6,3) NOT NULL DEFAULT 50 CHECK (crop_x BETWEEN 0 AND 100),
  crop_y NUMERIC(6,3) NOT NULL DEFAULT 50 CHECK (crop_y BETWEEN 0 AND 100),
  crop_zoom NUMERIC(5,3) NOT NULL DEFAULT 1 CHECK (crop_zoom BETWEEN 1 AND 4),
  rotation SMALLINT NOT NULL DEFAULT 0 CHECK (rotation IN (0, 90, 180, 270)),
  is_visible BOOLEAN NOT NULL DEFAULT TRUE,
  created_at TIMESTAMPTZ NOT NULL DEFAULT NOW(),
  updated_at TIMESTAMPTZ NOT NULL DEFAULT NOW(),
  UNIQUE (poster_id, asset_id)
);

CREATE INDEX IF NOT EXISTS idx_publicity_reading_poster_items_order
ON publicity_reading_poster_items(poster_id, is_visible, sort_order, id);

CREATE OR REPLACE FUNCTION app_private.validate_reading_media_asset()
RETURNS TRIGGER
LANGUAGE plpgsql
SET search_path = public
AS $$
DECLARE
  schedule_row study_schedule_instances%ROWTYPE;
  assigned_name TEXT;
  assigned_role TEXT;
BEGIN
  SELECT *
  INTO schedule_row
  FROM study_schedule_instances
  WHERE id = NEW.schedule_instance_id;

  IF NOT FOUND
     OR schedule_row.semester_id IS DISTINCT FROM NEW.semester_id
     OR schedule_row.org_id IS DISTINCT FROM NEW.org_id THEN
    RAISE EXCEPTION '共读影像必须关联同一学期和组织的日程';
  END IF;

  IF NEW.assignment_person_id IS NOT NULL THEN
    SELECT person.person_name_snapshot, demand.role_name
    INTO assigned_name, assigned_role
    FROM study_assignment_people person
    JOIN study_assignment_demands demand ON demand.id = person.demand_id
    WHERE person.id = NEW.assignment_person_id
      AND demand.schedule_instance_id = NEW.schedule_instance_id;

    IF assigned_name IS NULL THEN
      RAISE EXCEPTION '所选岗位人员不属于当前共读日程';
    END IF;

    NEW.demand_id = (
      SELECT demand_id
      FROM study_assignment_people
      WHERE id = NEW.assignment_person_id
    );
    NEW.person_name_snapshot = assigned_name;
    NEW.role_name_snapshot = COALESCE(assigned_role, '');
    NEW.asset_kind = 'role';
  ELSIF NEW.demand_id IS NOT NULL AND NOT EXISTS (
    SELECT 1
    FROM study_assignment_demands demand
    WHERE demand.id = NEW.demand_id
      AND demand.schedule_instance_id = NEW.schedule_instance_id
  ) THEN
    RAISE EXCEPTION '所选岗位不属于当前共读日程';
  END IF;

  NEW.caption = BTRIM(COALESCE(NEW.caption, ''));
  NEW.file_name = BTRIM(COALESCE(NEW.file_name, ''));
  NEW.updated_at = NOW();
  RETURN NEW;
END;
$$;

DROP TRIGGER IF EXISTS trg_validate_reading_media_asset ON study_reading_media_assets;
CREATE TRIGGER trg_validate_reading_media_asset
BEFORE INSERT OR UPDATE ON study_reading_media_assets
FOR EACH ROW EXECUTE FUNCTION app_private.validate_reading_media_asset();

CREATE OR REPLACE FUNCTION app_private.validate_publicity_reading_poster()
RETURNS TRIGGER
LANGUAGE plpgsql
SET search_path = public
AS $$
BEGIN
  IF NOT EXISTS (
    SELECT 1
    FROM study_schedule_instances schedule
    WHERE schedule.id = NEW.schedule_instance_id
      AND schedule.semester_id = NEW.semester_id
      AND schedule.org_id = NEW.org_id
  ) THEN
    RAISE EXCEPTION '晨读长图必须关联同一学期和组织的日程';
  END IF;

  NEW.title = BTRIM(COALESCE(NEW.title, ''));
  NEW.subtitle = BTRIM(COALESCE(NEW.subtitle, ''));
  NEW.closing_text = BTRIM(COALESCE(NEW.closing_text, ''));
  NEW.updated_at = NOW();
  RETURN NEW;
END;
$$;

