-- 已有宣委职务且已创建登录账号的人员，启用对应组织范围的宣委模块权限。
INSERT INTO module_memberships (
  user_id,
  semester_id,
  module_key,
  role,
  org_id,
  enabled
)
SELECT DISTINCT
  profile.id,
  position.semester_id,
  'publicity',
  position.position_name,
  position.org_id,
  TRUE
FROM person_positions position
JOIN people person ON person.id = position.person_id
JOIN profiles profile ON profile.phone = person.phone
WHERE position.is_active = TRUE
  AND position.position_name ILIKE '%宣委%'
  AND position.org_id IS NOT NULL
ON CONFLICT DO NOTHING;
