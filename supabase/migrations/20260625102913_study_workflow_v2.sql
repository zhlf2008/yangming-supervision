-- 5. 默认岗位种子：每个大班给一套常用岗位，可在前端继续维护
INSERT INTO study_roles (semester_id, org_id, role_name, role_type, sort_order)
SELECT o.semester_id, o.id, seed.role_name, seed.role_type, seed.sort_order
FROM organizations o
CROSS JOIN (
  VALUES
    ('主持人', '通用', 10),
    ('聆听者', '参与', 20),
    ('分享人', '参与', 30),
    ('回应砥砺', '参与', 40),
    ('总结赋能', '承办', 50),
    ('播控', '承办', 60),
    ('拍照', '承办', 70)
) AS seed(role_name, role_type, sort_order)
WHERE o.level = '大班'
ON CONFLICT (semester_id, org_id, role_name) DO NOTHING;

-- 6. RLS 与 Data API 显式授权
CREATE OR REPLACE FUNCTION app_private.has_study_membership_at_org(
  target_semester_id BIGINT,
  target_org_id BIGINT,
  allowed_roles TEXT[] DEFAULT ARRAY['学委', '副学委', '管理员', 'admin', 'manager']
)
RETURNS BOOLEAN
LANGUAGE sql
STABLE
SECURITY DEFINER
SET search_path = public
AS $$
  SELECT app_private.is_platform_admin()
    OR EXISTS (
      SELECT 1
      FROM module_memberships m
      WHERE m.user_id = auth.uid()
        AND m.semester_id = target_semester_id
        AND m.module_key = 'study'
        AND m.enabled = TRUE
        AND (allowed_roles IS NULL OR m.role = ANY(allowed_roles))
        AND (
          m.org_id = target_org_id
          OR (m.org_id IS NULL AND m.role IN ('管理员', 'admin', 'manager'))
        )
    );
$$;

REVOKE ALL ON FUNCTION app_private.has_study_membership_at_org(BIGINT, BIGINT, TEXT[]) FROM PUBLIC;
GRANT EXECUTE ON FUNCTION app_private.has_study_membership_at_org(BIGINT, BIGINT, TEXT[]) TO authenticated;

