    )
  );

CREATE POLICY "学委可管理日程内容" ON study_schedule_content FOR ALL
  TO authenticated
  USING (
    EXISTS (
      SELECT 1
      FROM study_schedule_instances si
      WHERE si.id = study_schedule_content.schedule_instance_id
        AND app_private.has_study_membership_at_org(si.semester_id, study_schedule_content.org_id, ARRAY['学委', '副学委', '管理员', 'admin', 'manager'])
    )
  )
  WITH CHECK (
    EXISTS (
      SELECT 1
      FROM study_schedule_instances si
      WHERE si.id = study_schedule_content.schedule_instance_id
        AND app_private.has_study_membership_at_org(si.semester_id, study_schedule_content.org_id, ARRAY['学委', '副学委', '管理员', 'admin', 'manager'])
    )
  );

