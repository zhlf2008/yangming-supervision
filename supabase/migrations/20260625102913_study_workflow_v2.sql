ON study_schedule_rules(semester_id, org_id, is_active);

-- 2. 岗位表：各大班维护可调用岗位
CREATE TABLE IF NOT EXISTS study_roles (
  id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
  semester_id BIGINT NOT NULL REFERENCES semesters(id) ON DELETE CASCADE,
  org_id BIGINT NOT NULL REFERENCES organizations(id) ON DELETE CASCADE,
  role_name TEXT NOT NULL,
  role_type TEXT NOT NULL DEFAULT '通用' CHECK (role_type IN ('通用', '承办', '参与')),
  sort_order INTEGER NOT NULL DEFAULT 0,
  is_active BOOLEAN NOT NULL DEFAULT TRUE,
  created_by UUID REFERENCES profiles(id) ON DELETE SET NULL,
  created_at TIMESTAMPTZ NOT NULL DEFAULT NOW(),
  updated_at TIMESTAMPTZ NOT NULL DEFAULT NOW()
);

CREATE UNIQUE INDEX IF NOT EXISTS idx_study_roles_unique
ON study_roles(semester_id, org_id, role_name);

CREATE INDEX IF NOT EXISTS idx_study_roles_active
ON study_roles(semester_id, org_id, is_active, sort_order, id);

-- 3. 日程内容覆盖表：保留实例表旧 custom 字段，新工作台优先读本表
CREATE TABLE IF NOT EXISTS study_schedule_content (
  id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
  schedule_instance_id BIGINT NOT NULL REFERENCES study_schedule_instances(id) ON DELETE CASCADE,
  org_id BIGINT NOT NULL REFERENCES organizations(id) ON DELETE CASCADE,
  course_id BIGINT REFERENCES study_course_library(id) ON DELETE SET NULL,
  custom_title TEXT,
  custom_theme TEXT,
  custom_content TEXT,
  custom_question TEXT,
  custom_paper_pages TEXT,
  custom_ebook_url TEXT,
  is_template BOOLEAN NOT NULL DEFAULT FALSE,
  note TEXT,
  created_by UUID REFERENCES profiles(id) ON DELETE SET NULL,
  created_at TIMESTAMPTZ NOT NULL DEFAULT NOW(),
  updated_at TIMESTAMPTZ NOT NULL DEFAULT NOW()
);

CREATE UNIQUE INDEX IF NOT EXISTS idx_study_schedule_content_unique
ON study_schedule_content(schedule_instance_id, org_id);

CREATE INDEX IF NOT EXISTS idx_study_schedule_content_org
ON study_schedule_content(org_id, schedule_instance_id);

