  'organization',
  CASE WHEN position.position_name ILIKE '%总组织委%' THEN '总组织委' ELSE '组织委' END,
  position.org_id,
  TRUE
FROM public.person_positions position
JOIN public.people person ON person.id = position.person_id
JOIN public.profiles profile ON profile.phone = person.phone
WHERE position.is_active = TRUE
  AND position.position_name ILIKE '%组织委%'
