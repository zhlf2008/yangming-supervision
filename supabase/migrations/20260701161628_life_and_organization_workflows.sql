INSERT INTO public.module_memberships (
  user_id, semester_id, module_key, role, org_id, enabled
)
SELECT DISTINCT
  profile.id,
  position.semester_id,
  'life',
  CASE WHEN position.position_name ILIKE '%总生活委%' THEN '总生活委' ELSE '生活委' END,
  position.org_id,
  TRUE
FROM public.person_positions position
JOIN public.people person ON person.id = position.person_id
JOIN public.profiles profile ON profile.phone = person.phone
WHERE position.is_active = TRUE
  AND position.position_name ILIKE '%生活委%'
  AND position.org_id IS NOT NULL
ON CONFLICT DO NOTHING;

INSERT INTO public.module_memberships (
