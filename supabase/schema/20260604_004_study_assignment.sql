  USING (
    EXISTS (SELECT 1 FROM profiles WHERE profiles.id = auth.uid() AND profiles.role IN ('超级管理员', '管理员'))
  )
  WITH CHECK (
    EXISTS (SELECT 1 FROM profiles WHERE profiles.id = auth.uid() AND profiles.role IN ('超级管理员', '管理员'))
  );

DROP POLICY IF EXISTS "认证用户可查看落位" ON study_assignment_people;
CREATE POLICY "认证用户可查看落位" ON study_assignment_people FOR SELECT
  USING (auth.uid() IS NOT NULL);
