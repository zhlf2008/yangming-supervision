// 提供 Supabase SDK（从 unpkg 缓存后提供）
// 避免从被墙的 CDN (jsdelivr) 加载

let sdkCache = null;

export async function onRequest() {
  if (!sdkCache) {
    const resp = await fetch('https://unpkg.com/@supabase/supabase-js@2/dist/umd/supabase.min.js');
    sdkCache = await resp.text();
  }

  return new Response(sdkCache, {
    headers: {
      'Content-Type': 'application/javascript',
      'Cache-Control': 'public, max-age=604800, immutable',
      'Access-Control-Allow-Origin': '*',
    },
  });
}
