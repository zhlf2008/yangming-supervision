// 提供 Supabase SDK（从 jsdelivr CDN 获取后缓存到内存）
// 避免用户直接从被墙的 CDN 加载

let sdkCache = null;
const SDK_URL = 'https://cdn.jsdelivr.net/npm/@supabase/supabase-js@2/dist/umd/supabase.min.js';

export async function onRequest() {
  if (!sdkCache) {
    const resp = await fetch(SDK_URL);
    if (!resp.ok) {
      return new Response(`SDK 加载失败: ${resp.status}`, { status: 502 });
    }
    sdkCache = await resp.text();
  }

  return new Response(sdkCache, {
    headers: {
      'Content-Type': 'application/javascript',
      'Cache-Control': 'public, max-age=3600',        // CDN 缓存 1 小时，不是 7 天
      'Access-Control-Allow-Origin': '*',
    },
  });
}
